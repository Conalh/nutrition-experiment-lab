"""Build docs/demo.gif — a slideshow tour of the captured screenshots.

We can't record live video without ffmpeg, so this stitches the real
screenshots (generated by `npm run screenshots`) into a looping GIF that
renders inline on GitHub. Run: python scripts/make_demo_gif.py
"""
from __future__ import annotations

from pathlib import Path

from PIL import Image

SHOTS = Path(__file__).resolve().parents[1] / "docs" / "screenshots"
OUT = Path(__file__).resolve().parents[1] / "docs" / "demo.gif"

# A natural tour through the app.
FRAMES = ["login", "onboarding", "builder", "daily-log", "detail", "report"]
WIDTH = 960
FRAME_MS = 1900


def load(name: str) -> Image.Image:
    img = Image.open(SHOTS / f"{name}.png").convert("RGB")
    h = round(img.height * WIDTH / img.width)
    img = img.resize((WIDTH, h), Image.LANCZOS)
    # Adaptive 256-colour palette keeps the GIF small while holding the
    # dark surfaces + amber accent reasonably well.
    return img.quantize(colors=256, method=Image.MEDIANCUT)


def main() -> None:
    frames = [load(n) for n in FRAMES]
    frames[0].save(
        OUT,
        save_all=True,
        append_images=frames[1:],
        duration=FRAME_MS,
        loop=0,
        optimize=True,
        disposal=2,
    )
    kb = OUT.stat().st_size / 1024
    print(f"wrote {OUT} ({kb:.0f} KB, {len(frames)} frames)")


if __name__ == "__main__":
    main()
